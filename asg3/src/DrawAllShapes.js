function drawMap(){
   for(x=0; x<32; x++){
      for(y=0; y<32; y++){
         if ((x == 0 || x == 31) && y%4 == 0){
            var wall = new Cube();
            wall.color = [.80, .70, .40, 1.0];
            wall.textureNum = -2;
            wall.matrix.scale(0.25,0.25,0.25);
            wall.matrix.translate(x-16,-.25,y-16);
            wall.renderfast();
         }
         if ((x == 0 || x == 31) && y%4 != 0){
            var wall = new Cube();
            wall.color = [.60, .40, .20, 1.0];
            wall.textureNum = -2;
            wall.matrix.scale(0.25,0.73,0.25);
            wall.matrix.translate(x-16,-.25,y-16);
            wall.renderfast();
         }
         if ((y == 0 || y == 31) && x%4 == 0){
            var wall = new Cube();
            wall.color = [.80, .70, .50, 1.0];
            wall.textureNum = -2;
            wall.matrix.scale(0.25,0.25,0.25);
            wall.matrix.translate(x-16,-.25,y-16);
            wall.renderfast();
         }
         if ((y == 0 || y == 31) && x%4 != 0){
            var wall = new Cube();
            wall.color = [.60, .40, .20, 1.0];
            wall.textureNum = -2;
            wall.matrix.scale(0.25,0.73,0.25);
            wall.matrix.translate(x-16,-.25,y-16);
            wall.renderfast();
         }
      }
   }
}

function drawAllShapes(){
   drawMap();
   // Sky
   var sky = new Cube();
   sky.color = [.6, .9, .95, 1];
   sky.textureNum = 1;
   sky.matrix.scale(10,10,10);
   sky.matrix.translate(-.5, -.5, -.5);
   sky.render();

   // Floor
   var floor = new Cube();
   floor.color = [.2, .9, .4, 1];
   floor.textureNum = 0;
   floor.matrix.translate(0,-.25,0);
   floor.matrix.scale(10,0,10);
   floor.matrix.translate(-.5, 0, -.5);
   floor.render();

}