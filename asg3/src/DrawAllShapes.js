 const BLOCK_SIZE = 1.0; // Each cube is 1x1x1 world units
 const BLOCK_HEIGHT = 2; // Walls are 2 cubes high
 const DIRT_INDEX = 2; 

function drawMaze() {
   const groundLevel = -0.25;
   const mazeRows = g_map.length;
   const mazeCols = g_map[0].length;
   const offsetX = -( (mazeCols -1) / 2.0) * BLOCK_SIZE;
   const offsetZ = -( (mazeRows -1) / 2.0) * BLOCK_SIZE;

   for (let r = 0; r < mazeRows; r++) {
       for (let c = 0; c < mazeCols; c++) {
           if (g_map[r][c] === 1) {
               for (let h = 0; h < BLOCK_HEIGHT; h++) {
                   let wallBlock = new Cube();
                   wallBlock.textureNum = DIRT_INDEX;

                   let worldX = c * BLOCK_SIZE + offsetX;
                   let worldZ = r * BLOCK_SIZE + offsetZ;
                   let worldY = groundLevel + (h * BLOCK_SIZE) + (BLOCK_SIZE / 2);

                   wallBlock.matrix = new Matrix4();
                   wallBlock.matrix.translate(worldX, worldY, worldZ);
                   wallBlock.matrix.scale(BLOCK_SIZE, BLOCK_SIZE, BLOCK_SIZE);
                   wallBlock.matrix.translate(-0.5, -0.5, -0.5);

                   wallBlock.render(); 
               }
           }
       }
   }
}

function drawAllShapes(){
   drawMaze();

   // Sky
   var sky = new Cube();
   sky.color = [.6, .9, .95, 1];
   sky.textureNum = 1;
   sky.matrix = new Matrix4();
   sky.matrix.translate(0, 15.5, 0);
   sky.matrix.scale(32, 32, 32);
   sky.matrix.translate(-0.5, -0.5, -0.5);
   sky.render();

   // Floor
   var floor = new Cube();
   floor.color = [.2, .9, .4, 1];
   floor.textureNum = 0;
   floor.matrix.translate(0, -0.25, 0);
   floor.matrix.scale(32, 0.01, 32);
   floor.matrix.translate(-.5, -15.75, -.5);

   // Floor thickness
   var floorThickness = 0.05;
   floor.matrix = new Matrix4();
   floor.matrix.translate(0, -0.25 - (floorThickness/2), 0);
   floor.matrix.scale(32, floorThickness, 32);
   floor.matrix.translate(-0.5, -0.5, -0.5);
   floor.render();

   // Trunk
   var trunk = new Cube();
   trunk.color = [0.5, 0.35, 0.2, 1.0];
   trunk.textureNum = -2;
   var trunkMatrix = new Matrix4();
   trunkMatrix.translate(-0.1, -0.25, -0.1);
   trunkMatrix.scale(0.2, 1.0, 0.2);
   trunk.matrix = trunkMatrix;
   trunk.renderfast();

   // Leaves
   var leaves = new Cube();
   leaves.color = [0.2, 0.6, 0.1, 1.0];
   leaves.textureNum = -2;
   var leavesMatrix = new Matrix4();
   leavesMatrix.translate(-0.3, 0.75, -0.3);
   leavesMatrix.scale(0.6, 0.6, 0.6);
   leaves.matrix = leavesMatrix;
   leaves.renderfast();

   for (let i = 0; i < g_placedBlocks.length; i++) {
       g_placedBlocks[i].renderfast();
   }

   if (typeof g_enemies !== 'undefined') {
      for (let i = 0; i < g_enemies.length; i++) {
            g_enemies[i].renderfast();
      }
   }
}